def get_flights(origin: str, dest: str) -> list:
    key = f"{origin}-{dest}"
    raw = ROUTES.get(key, [])
    return [
        {
            "flight_no": r[0], "airline": r[1],
            "airline_name": AIRLINES.get(r[1], r[1]),
            "departure": r[2], "arrival": r[3],
            "aircraft": r[4],
            "departure_hour": int(r[2].split(":")[0]),
            "route": key,
        }
        for r in raw
    ]
